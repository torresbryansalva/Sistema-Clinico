    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
    Connectar conx=new Connectar("localhost", "dbclinica", "root", "");
    String sql="";
      
    String espe=(String) cmb_especial.getSelectedItem();
    int idoc=Integer.parseInt(txt_idDoc.getText());
    int dnip=Integer.parseInt(txt_dnni.getText());
    String nom=txt_nom.getText();
    String apll=txt_apell.getText();
    int id = Integer.parseInt(txt_id.getText());
    int edd= Integer.parseInt(txt_edad.getText());
    String segur=txt_seguro.getText();
    String comen=txt_comentarios.getText();
    
    sql="INSERT INTO doctores (id_doctor,Especid,dni_pnt,nomb,apelli,id,edad,seguro,comentario) VALUES (?,?,?,?,?,?,?,?,?)";
    
        try {
            Connection cm=conx.conexion();
            PreparedStatement pmn =cm.prepareStatement(sql);
        pmn.setInt(1,idoc);
        pmn.setString(2,espe);
        pmn.setInt(3,dnip);
        pmn.setString(4,nom);
        pmn.setString(5,apll);
        pmn.setInt(6, id);
        pmn.setInt(7,edd);
        pmn.setString(8,segur);
        pmn.setString(9,comen);
        
        int n=pmn.executeUpdate();
        if(n>0){
                JOptionPane.showMessageDialog(null, "guardado con exito!!!");
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btn_guardarActionPerformed

