    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
     
        Connectar conx=new Connectar("localhost", "dbclinica", "root", "");
            String sql="";
            Connection cm;
            ResultSet rs;
        sql="SELECT * FROM citas WHERE dni = ?";
        try {
            cm = conx.conexion();
             PreparedStatement pmn =cm.prepareStatement(sql);
             pmn.setString(1, txtdni.getText());
             rs=pmn.executeQuery();
        
        if(rs.next()){
             //txtId.setText(rs.getString("id"));
            txt_dnni.setText(rs.getString("dni"));
            txt_nom.setText(rs.getString("nom"));
            txt_apell.setText(rs.getString("apell"));
            txt_edad.setText(rs.getString("edad"));
            txt_seguro.setText(rs.getString("seguro"));
       }else{
        JOptionPane.showMessageDialog(null, "No existe una persona con la clave");    
        }
           
        } catch (SQLException ex) {
            Logger.getLogger(Doctor.class.getName()).log(Level.SEVERE, null, ex);
        }
           
       
    }//GEN-LAST:event_jButton2ActionPerformed

